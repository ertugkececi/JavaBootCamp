package conditionals;

public class Main {

	public static void main(String[] args) {
		int number = 19;
		
		if (number < 20) {
			System.out.println("Number is smaller than 20");			
		} else if (number == 20) {
			System.out.println("Number is equal to 20");
		} else {
			System.out.println("Number is bigger than 20");
		}
	}

}
